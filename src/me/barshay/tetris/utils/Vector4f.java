package me.barshay.tetris.utils;

public class Vector4f {
		public float r,g,b,a;
		public Vector4f(float r, float g, float b, float a) {
			this.r = r;
			this.g = g;
			this.b = b;
			this.a = a;
		}
		public Vector4f() {
			this.r = 0.0f;
			this.g = 0.0f;
			this.b = 0.0f;
			this.a = 1.0f;
		}
}

